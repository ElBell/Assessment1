from typing import List

class BasicArrayUtils:

    @staticmethod
    def get_first_element(string_array: List[str]) -> str:
        return string_array[0]

    @staticmethod
    def get_second_element(string_array: List[str]) -> str:
        return string_array[1]

    @staticmethod
    def get_last_element(string_array: List[str]) -> str:
        return string_array[-1]

    @staticmethod
    def get_second_to_last_element(string_array: List[str]) -> str:
        return string_array[-2]

